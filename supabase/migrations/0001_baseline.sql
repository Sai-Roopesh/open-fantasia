create or replace function public.set_default_persona(
  target_persona_id uuid,
  target_user_id uuid
)
returns setof public.user_personas
language plpgsql
set search_path = public
as $$
begin
  if not exists (
    select 1
    from public.user_personas personas
    where personas.id = target_persona_id
      and personas.user_id = target_user_id
  ) then
    raise exception 'Persona % not found for user %', target_persona_id, target_user_id;
  end if;

  update public.user_personas
  set
    is_default = (id = target_persona_id),
    updated_at = now()
  where user_id = target_user_id
    and (is_default = true or id = target_persona_id);

  return query
  select *
  from public.user_personas personas
  where personas.user_id = target_user_id
  order by personas.is_default desc, personas.updated_at desc;
end;
$$;

create or replace function public.list_persona_usage(
  p_user_id uuid
)
returns table (
  persona_id uuid,
  total_threads bigint,
  active_threads bigint
)
language sql
set search_path = public
as $$
  select
    threads.persona_id,
    count(*)::bigint as total_threads,
    count(*) filter (where threads.status = 'active')::bigint as active_threads
  from public.chat_threads threads
  where threads.user_id = p_user_id
    and threads.persona_id is not null
  group by threads.persona_id;
$$;

create or replace function public.search_thread_library(
  p_user_id uuid,
  p_status text default 'all',
  p_query text default null,
  p_limit integer default null
)
returns table (
  id uuid,
  user_id uuid,
  character_id uuid,
  connection_id uuid,
  model_id text,
  persona_id uuid,
  active_branch_id uuid,
  title text,
  is_title_autogenerated boolean,
  status text,
  archived_at timestamptz,
  pinned_at timestamptz,
  temperature_override numeric,
  top_p_override numeric,
  max_output_tokens_override integer,
  created_at timestamptz,
  updated_at timestamptz,
  character_name text,
  persona_name text
)
language plpgsql
set search_path = public
as $$
declare
  normalized_query text := nullif(trim(coalesce(p_query, '')), '');
  applied_limit integer := case
    when p_limit is null or p_limit < 1 then null
    else p_limit
  end;
begin
  if auth.uid() is distinct from p_user_id then
    raise exception 'User mismatch for thread search.';
  end if;

  if p_status not in ('active', 'archived', 'all') then
    raise exception 'Unsupported thread status filter: %', p_status;
  end if;

  return query
  select
    threads.id,
    threads.user_id,
    threads.character_id,
    threads.connection_id,
    threads.model_id,
    threads.persona_id,
    threads.active_branch_id,
    threads.title,
    threads.is_title_autogenerated,
    threads.status,
    threads.archived_at,
    threads.pinned_at,
    threads.temperature_override,
    threads.top_p_override,
    threads.max_output_tokens_override,
    threads.created_at,
    threads.updated_at,
    characters.name as character_name,
    personas.name as persona_name
  from public.chat_threads threads
  join public.characters characters
    on characters.id = threads.character_id
   and characters.user_id = p_user_id
  left join public.user_personas personas
    on personas.id = threads.persona_id
   and personas.user_id = p_user_id
  where threads.user_id = p_user_id
    and threads.active_branch_id is not null
    and (p_status = 'all' or threads.status = p_status)
    and (
      normalized_query is null
      or threads.title ilike '%' || normalized_query || '%'
      or characters.name ilike '%' || normalized_query || '%'
      or coalesce(personas.name, '') ilike '%' || normalized_query || '%'
    )
  order by threads.pinned_at desc nulls last, threads.updated_at desc
  limit applied_limit;
end;
$$;

create or replace function public.delete_persona_and_reassign(
  p_user_id uuid,
  p_persona_id uuid
)
returns uuid
language plpgsql
set search_path = public
as $$
declare
  target_persona public.user_personas;
  replacement_persona public.user_personas;
  used_in_threads bigint := 0;
begin
  select *
  into target_persona
  from public.user_personas personas
  where personas.id = p_persona_id
    and personas.user_id = p_user_id;

  if target_persona.id is null then
    return null;
  end if;

  select *
  into replacement_persona
  from public.user_personas personas
  where personas.user_id = p_user_id
    and personas.id <> p_persona_id
  order by personas.is_default desc, personas.updated_at desc
  limit 1;

  select count(*)
  into used_in_threads
  from public.chat_threads threads
  where threads.user_id = p_user_id
    and threads.persona_id = p_persona_id;

  if used_in_threads > 0 and replacement_persona.id is null then
    raise exception 'Create another persona before deleting the only persona used by your threads.';
  end if;

  if replacement_persona.id is not null then
    update public.chat_threads
    set
      persona_id = replacement_persona.id,
      updated_at = now()
    where user_id = p_user_id
      and persona_id = p_persona_id;

    if target_persona.is_default then
      update public.user_personas
      set
        is_default = false,
        updated_at = now()
      where user_id = p_user_id
        and is_default = true;

      update public.user_personas
      set
        is_default = true,
        updated_at = now()
      where id = replacement_persona.id
        and user_id = p_user_id;
    end if;
  end if;

  delete from public.user_personas personas
  where personas.id = p_persona_id
    and personas.user_id = p_user_id;

  return replacement_persona.id;
end;
$$;

