create or replace function public.claim_background_jobs(limit_count integer default 10)
returns setof public.background_jobs
language plpgsql
security definer
set search_path = public
as $$
begin
  return query
  with selected as (
    select jobs.id
    from public.background_jobs jobs
    where jobs.status = 'pending'
      and jobs.available_at <= now()
      and (jobs.locked_at is null or jobs.locked_at < now() - interval '10 minutes')
    order by jobs.available_at asc, jobs.created_at asc
    limit limit_count
    for update skip locked
  ),
  updated as (
    update public.background_jobs jobs
    set
      status = 'running',
      attempts = jobs.attempts + 1,
      locked_at = now(),
      updated_at = now()
    where jobs.id in (select id from selected)
    returning jobs.*
  )
  select * from updated;
end;
$$;

