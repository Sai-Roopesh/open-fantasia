export async function maybeAutotitleThreadFromMessage(
  supabase: DatabaseClient,
  userId: string,
  thread: ThreadRecord,
  text: string,
) {
  if (!thread.is_title_autogenerated) {
    return thread;
  }

  const nextTitle = text.trim().slice(0, 60).trim();
  if (!nextTitle) {
    return thread;
  }

  const { data, error } = await supabase
    .from("chat_threads")
    .update({
      title: nextTitle,
      is_title_autogenerated: false,
      updated_at: new Date().toISOString(),
    })
    .eq("id", thread.id)
    .eq("user_id", userId)
    .select(threadSelect)
    .single();

  if (error) throw error;
  return castRow<ThreadRecord>(data);
}

