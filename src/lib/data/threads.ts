export function resolveSnapshotState(
  checkpoints: ChatCheckpointRecord[],
  snapshotsByCheckpointId: Map<string, ThreadStateSnapshot>,
  latestReconcileJob?: Pick<BackgroundJobRecord, "status" | "last_error"> | null,
) {
  const latestCheckpoint = checkpoints.at(-1) ?? null;
  const headSnapshot = latestCheckpoint
    ? snapshotsByCheckpointId.get(latestCheckpoint.id) ?? null
    : null;
  const resolvedSnapshot =
    headSnapshot ??
    [...checkpoints]
      .reverse()
      .map((checkpoint) => snapshotsByCheckpointId.get(checkpoint.id) ?? null)
      .find((snapshot): snapshot is ThreadStateSnapshot => Boolean(snapshot)) ??
    null;
  const headSnapshotFailed = Boolean(
    latestCheckpoint &&
      !headSnapshot &&
      latestReconcileJob?.status === "failed",
  );

  return {
    headSnapshot,
    resolvedSnapshot,
    headSnapshotPending: Boolean(latestCheckpoint && !headSnapshot && !headSnapshotFailed),
    headSnapshotFailed,
    headSnapshotFailureMessage: headSnapshotFailed
      ? latestReconcileJob?.last_error ?? "Continuity reconciliation failed for the branch head."
      : null,
  };
}

function sortThreads<T extends ThreadRecord>(threads: T[]) {
  return [...threads].sort((left, right) => {
    const leftPinned = left.pinned_at ? new Date(left.pinned_at).getTime() : 0;
    const rightPinned = right.pinned_at ? new Date(right.pinned_at).getTime() : 0;
    if (leftPinned !== rightPinned) return rightPinned - leftPinned;
    return new Date(right.updated_at).getTime() - new Date(left.updated_at).getTime();
  });
}

export async function listThreads(supabase: DatabaseClient, userId: string) {
  return listThreadItems(supabase, userId, { status: "active" });
}

export async function listThreadItems(
  supabase: DatabaseClient,
  userId: string,
  options?: {
    query?: string;
    status?: "active" | "archived" | "all";
  },
) {
  const status = options?.status ?? "all";
  let queryBuilder = supabase
    .from("chat_threads")
    .select(threadListSelect)
    .eq("user_id", userId)
    .not("active_branch_id", "is", null)
    .order("updated_at", { ascending: false });

  if (status !== "all") {
    queryBuilder = queryBuilder.eq("status", status);
  }

  const { data, error } = await queryBuilder;

  if (error) throw error;
  const raw = castRows<ThreadListItem>(data, "Thread list items");
  const query = options?.query?.trim().toLowerCase() ?? "";

  return sortThreads(
    raw.filter((thread) => {
      if (!query) return true;
      return [thread.title, thread.characters?.name, thread.user_personas?.name]
        .filter(Boolean)
        .some((value) => value!.toLowerCase().includes(query));
    }),
  );
}

export async function getThread(
  supabase: DatabaseClient,
  userId: string,
  threadId: string,
) {
  const { data, error } = await supabase
    .from("chat_threads")
    .select(threadSelect)
    .eq("user_id", userId)
    .eq("id", threadId)
    .maybeSingle();

  if (error) throw error;
  return data ? castRow<ThreadRecord>(data) : null;
}

export async function createThread(
  supabase: DatabaseClient,
  userId: string,
  args: {
    characterId: string;
    connection: ConnectionRecord;
    modelId: string;
    title: string;
    personaId: string;
  },
) {
  const { data, error } = await supabase.rpc("create_thread_with_main_branch", {
    p_user_id: userId,
    p_character_id: args.characterId,
    p_connection_id: args.connection.id,
    p_model_id: args.modelId,
    p_persona_id: args.personaId,
    p_title: args.title,
  });

  if (error) throw error;
  return castRow<ThreadRecord>(data, "Created thread");
}

