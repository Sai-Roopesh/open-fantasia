export async function getThreadGraphView(
  supabase: DatabaseClient,
  userId: string,
  threadId: string,
): Promise<ThreadGraphView | null> {
  const thread = await getThread(supabase, userId, threadId);
  if (!thread || !thread.active_branch_id) return null;

  const [branches, checkpoints] = await Promise.all([
    listBranches(supabase, userId, threadId),
    listCheckpoints(supabase, userId, threadId),
  ]);

  const activeBranch = branches.find((branch) => branch.id === thread.active_branch_id);
  if (!activeBranch) return null;

  const checkpointPath = buildCheckpointPath(checkpoints, activeBranch.head_checkpoint_id);
  const latestCheckpoint = checkpointPath.at(-1) ?? null;
  const messageIds = checkpointPath.flatMap((checkpoint) => [
    checkpoint.user_message_id,
    checkpoint.assistant_message_id,
  ]);
  const checkpointIds = checkpointPath.map((checkpoint) => checkpoint.id);
  const [messages, snapshots, latestReconcileJob] = await Promise.all([
    getMessagesByIds(supabase, messageIds),
    listSnapshotsByCheckpointIds(supabase, checkpointIds),
    latestCheckpoint
      ? getLatestReconcileJobForCheckpoint(supabase, latestCheckpoint.id)
      : Promise.resolve(null),
  ]);
  const messagesById = new Map(messages.map((message) => [message.id, message]));
  const snapshotsByCheckpointId = new Map(
    snapshots.map((snapshot) => [snapshot.checkpoint_id, snapshot]),
  );

  const canonicalStoredMessages: StoredMessageRecord[] = [];
  const controlsByMessageId: Record<string, TranscriptControl> = {};

  checkpointPath.forEach((checkpoint, index) => {
    const userMessage = messagesById.get(checkpoint.user_message_id);
    const assistantMessage = messagesById.get(checkpoint.assistant_message_id);
    if (!userMessage || !assistantMessage) return;

    const isLatest = index === checkpointPath.length - 1;

    if (!userMessage.metadata?.hiddenFromTranscript) {
      controlsByMessageId[userMessage.id] = {
        checkpointId: checkpoint.id,
        branchId: checkpoint.branch_id,
        canEdit: isLatest,
        canRegenerate: false,
        canBranch: true,
        canRewind: true,
        canPin: true,
        canRate: false,
        feedbackRating: checkpoint.feedback_rating,
      };
    }

    controlsByMessageId[assistantMessage.id] = {
      checkpointId: checkpoint.id,
      branchId: checkpoint.branch_id,
      canEdit: false,
      canRegenerate: isLatest,
      canBranch: false,
      canRewind: false,
      canPin: true,
      canRate: true,
      feedbackRating: checkpoint.feedback_rating,
    };

    canonicalStoredMessages.push(userMessage, assistantMessage);
  });

  const [rawTimeline, rawPins] = await Promise.all([
    listTimeline(supabase, threadId, activeBranch.id, 24),
    listPins(supabase, threadId, activeBranch.id),
  ]);
  const reachableCheckpointIds = new Set(checkpointPath.map((checkpoint) => checkpoint.id));
  const reachableMessageIds = new Set(canonicalStoredMessages.map((message) => message.id));
  const timeline = rawTimeline
    .filter((event) => {
      if (event.checkpoint_id) return reachableCheckpointIds.has(event.checkpoint_id);
      if (event.source_message_id) return reachableMessageIds.has(event.source_message_id);
      return true;
    })
    .slice(0, 8);
  const pins = rawPins.filter(
    (pin) => !pin.source_message_id || reachableMessageIds.has(pin.source_message_id),
  );

  const {
    headSnapshot,
    resolvedSnapshot,
    headSnapshotPending,
    headSnapshotFailed,
    headSnapshotFailureMessage,
  } = resolveSnapshotState(
    checkpointPath,
    snapshotsByCheckpointId,
    latestReconcileJob,
  );

  return {
    thread,
    branches,
    activeBranch,
    checkpoints: checkpointPath,
    latestCheckpoint,
    headSnapshot,
    resolvedSnapshot,
    headSnapshotPending,
    headSnapshotFailed,
    headSnapshotFailureMessage,
    timeline,
    pins,
    canonicalMessages: toUIMessages(canonicalStoredMessages),
    modelContextMessages: toUIMessages(canonicalStoredMessages, { includeHidden: true }),
    controlsByMessageId,
  };
}

export function buildCheckpointPath(
  checkpoints: ChatCheckpointRecord[],
  headCheckpointId: string | null,
) {
  if (!headCheckpointId) return [];
  const checkpointsById = new Map(checkpoints.map((checkpoint) => [checkpoint.id, checkpoint]));
  const path: ChatCheckpointRecord[] = [];
  let pointer: string | null = headCheckpointId;

  while (pointer) {
    const checkpoint = checkpointsById.get(pointer);
    if (!checkpoint) break;
    path.push(checkpoint);
    pointer = checkpoint.parent_checkpoint_id;
  }

  return path.reverse();
}

export function createTextMessage(args: {
  id?: string;
  role: "user" | "assistant" | "system";
  text: string;
  metadata?: MessageMetadata;
}) {
  return {
    id: args.id ?? crypto.randomUUID(),
    role: args.role,
    parts: args.text
      ? [
          {
            type: "text" as const,
            text: args.text,
          },
        ]
      : [],
    metadata: args.metadata ?? {},
  } satisfies FantasiaUIMessage;
}

export function truncateMessageText(message: Pick<FantasiaUIMessage, "parts">, length = 120) {
  const text = getTextFromMessageParts(message.parts);
  if (text.length <= length) return text;
  return `${text.slice(0, length).trim()}...`;
}
